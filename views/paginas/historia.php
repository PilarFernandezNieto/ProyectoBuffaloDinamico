        <div class="texto-historia caja2 mt-3">
            <p><b>The Electric Buffalo</b> (Oviedo) surge, como idea, en un garito de costumbres rockeras allá por el año 2006, en
                Oviedo. Álvaro y Wilón, fans estudiosos y entusiastas de los <b>Gov’t Mule</b>, acuerdan, entre vapores etílicos
                y con el Deepest End de fondo toda la noche (cortesía de Chiri y Noe, porque en su casa, el <b>Sweet Home</b>, sabían cuidar bien de su gente), formar una banda con una única condición: hacer exactamente lo que les dé la gana sin ninguna prisa ni pretensión.</p>
            <div>
                <p>
                    Ahí surgió la idea, pero faltaba la banda. El siguiente paso fue buscar un bajista y decidieron tantear a
                    <b>Alejandro “Espina”</b>. Jandro, bajista de <b>Ilegales</b> por aquella época, era muy conocido también por haber
                    sido profesor de bajo en el <b>Taller de Músicos de Oviedo</b> y por suministrar risas, comedia a paladas,
                    cervezas y música en otro garito rockero y molón de la calle Martínez Vigil de Oviedo, <b>La Armónica Blues Bar</b>, que regentaba su mujer, Irene.
                </p>
                <p>No se hizo de rogar. Álvaro se lo planteó y Jandro dijo “sí” al momento.</p>
            </div>
        </div>
